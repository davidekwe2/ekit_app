      'fr': 'FR',
      'de': 'DE',
      'it': 'IT',
      'pt': 'BR',
      'ru': 'RU',
      'ar': 'SA',
      'zh': 'CN',
      'ja': 'JP',
      'ko': 'KR',
      'hi': 'IN',
      'nl': 'NL',
      'sv': 'SE',
      'pl': 'PL',
    };
    
    final defaultCountry = defaultCountries[locale.languageCode] ?? 'US';
    final keyWithDefault = '${locale.languageCode}_$defaultCountry';
    if (_localizedValues.containsKey(keyWithDefault)) {
      return keyWithDefault;
    }
    
    // Fallback to en_US if nothing matches
    return 'en_US';
